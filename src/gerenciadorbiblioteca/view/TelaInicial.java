    public TelaInicial() throws FileNotFoundException {
        livrosTabela = new ArrayList(livroDao.lerArquivo());
        quadrinhosTabela = new ArrayList(quadrinhoDao.lerArquivo());
        prateleiras = new ArrayList(prateleiraDao.lerArquivo());
        estantes = new ArrayList(estanteDao.lerArquivo());
        
        
        for(int i=0;i<livrosTabela.size();i++){
            livroTableModel.adcicionarLivro(livrosTabela.get(i));
        }
        for(int i=0;i<quadrinhosTabela.size();i++){
            quadrinhoTableModel.adcicionarQuadrinho(quadrinhosTabela.get(i));
        }     
        
        
        initComponents();
        jTableLivros.setModel(livroTableModel);
        jTableQuadrinhos.setModel(quadrinhoTableModel);
        
        for(int i=0;i<prateleiras.size();i++){
            prateleirasLivro.addItem(prateleiras.get(i).getNome());
            prateleirasQuadrinho.addItem(prateleiras.get(i).getNome());
        }
        for(int i=0;i<prateleiras.size();i++){
           selectEstantes.addItem(estantes.get(i).getNome());
        }
        
    }

