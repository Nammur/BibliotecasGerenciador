    private void btnNovoLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoLivroActionPerformed
        if((txtAutor.getText() != "") && txtGenero.getText() != "" && txtISBN.getText() != ""
                && txtNomeNovoLivro.getText() != "" && txtNumeroPaginas.getText() != ""){
            try {
                Livro livro = new Livro();
                livro.setTitulo(txtNomeNovoLivro.getText());
                livro.setAutor(txtAutor.getText());
                livro.setCapaDura(cBCapaDura.isSelected());
                livro.setGenero(txtGenero.getText());
                livro.setNumeroPaginas(Integer.parseInt(txtNumeroPaginas.getText()));
                livro.setPrateleira(prateleirasLivro.getSelectedItem().toString());
                livro.setISBN(txtISBN.getText());
                livro.setId(ctrlUtil.getIdIncr());
                
                livroDao.salvar(livro,true);
                livroTableModel.adcicionarLivro(livro);
                jTableLivros.addNotify();
                
                txtAutor.setText("");
                txtGenero.setText("");
                txtISBN.setText("");
                txtNomeNovoLivro.setText("");
                txtNumeroPaginas.setText("");
                
            } catch (ExceptionGeral ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }     
        // TODO add your handling code here:
    }//GEN-LAST:event_btnNovoLivroActionPerformed

    private void btnCriarEstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarEstActionPerformed
        if(txtNomeNovaEstante.getText() != null){
            try {
                Estante estante = new Estante();
                estante.setNome(txtNomeNovaEstante.getText());
                
                estanteDao.salvar(estante);
                estantes.add(estante);
                selectEstantes.addItem(estante.getNome());
                
                txtNomeNovaEstante.setText("");
            } catch (ExceptionGeral ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        
        }
    }//GEN-LAST:event_btnCriarEstActionPerformed

    private void btnCriarPratActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarPratActionPerformed
        if(txtNomeNovaPrateleira.getText() != null){
            try {
                Prateleira prateleira = new Prateleira();
                prateleira.setNome(txtNomeNovaPrateleira.getText());
                prateleira.setEstante(selectEstantes.getSelectedItem().toString());
                
                prateleiraDao.salvar(prateleira);
                prateleiras.add(prateleira);
                
                prateleirasLivro.addItem(prateleira.getNome());
                prateleirasQuadrinho.addItem(prateleira.getNome());
                
                txtNomeNovaPrateleira.setText("");
            } catch (ExceptionGeral ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCriarPratActionPerformed

    private void btnCriarQuadrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarQuadrinhoActionPerformed
        if((txtAutorQuadrinho.getText() == null) || (txtGeneroQuadrinho.getText() == null) || (txtISBNQuadrinho.getText() == null)
                || (txtNomeNovoLQuadrinho.getText() == null) || (txtTipo.getText() == null)){
        }
        else{
            try {
                Quadrinho quadrinho = new Quadrinho();
                
                quadrinho.setTitulo(txtNomeNovoLQuadrinho.getText());
                quadrinho.setAutor(txtAutorQuadrinho.getText());
                quadrinho.setColorido(cBColorido.isSelected());
                quadrinho.setGenero(txtGeneroQuadrinho.getText());
                quadrinho.setTipo(txtTipo.getText());
                quadrinho.setPrateleira(prateleirasQuadrinho.getSelectedItem().toString());
                quadrinho.setISBN(txtISBNQuadrinho.getText());
                quadrinho.setId(ctrlUtil.getIdIncr());
                
                quadrinhoDao.salvar(quadrinho);
                quadrinhoTableModel.adcicionarQuadrinho(quadrinho);
                jTableQuadrinhos.addNotify();
                
                txtAutorQuadrinho.setText("");
                txtGeneroQuadrinho.setText("");
                txtISBNQuadrinho.setText("");
                txtNomeNovoLQuadrinho.setText("");
                txtTipo.setText("");
                
            } catch (ExceptionGeral ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }
    }//GEN-LAST:event_btnCriarQuadrinhoActionPerformed

    private void btnDeletarLivrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarLivrosActionPerformed
        int[] indiceLivrosSelecionados = jTableLivros.getSelectedRows();
        try {
            livroDao.deletar(indiceLivrosSelecionados);
        } catch (ExceptionGeral ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        for(int i=0;i<indiceLivrosSelecionados.length;i++){
            livroTableModel.fireTableRowsDeleted(indiceLivrosSelecionados[i],indiceLivrosSelecionados[i] );
        }
        
        
        jTableLivros.addNotify();
        
    }//GEN-LAST:event_btnDeletarLivrosActionPerformed

